/**
 * op_add - function to add two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: the product of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to divide two integers
 * @a: first integer
 * @b: second integer
 * Return: the division of the two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to mod two integers
 * @a: first integer
 * @b: second integer
 * Return: the remainder of the two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
